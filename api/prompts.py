def setup_all_prompts(profile: dict, api_client,
                      overwrite: bool = False) -> dict:
    """
    One call that does everything:
      1. Seeds the generic prompts (intake_questions, intake_xml_builder)
      2. Generates the personal prompts (relevance_check, resume_tailor)

    This is the only function resume_intake.py and migrate_prompts.py
    need to call. Users never call this directly.

    Returns total {'created': n, 'skipped': n}.
    """
    g = seed_generic_prompts()
    p = generate_personal_prompts(profile, api_client, overwrite=overwrite)
    return {
        "created": g["created"] + p["created"],
        "skipped": g["skipped"] + p["skipped"],
    }