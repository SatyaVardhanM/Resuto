def seed_generic_prompts() -> dict:
    """
    Seeds all generic prompts (same for every user).
    Safe to call multiple times -- never overwrites existing prompts.
    Returns {'created': n, 'skipped': n}.
    """
    created = 0
    skipped = 0

    for name, content, desc in [
        (PROMPT_INTAKE_QUESTIONS, _GENERIC_INTAKE_QUESTIONS,
         "Generates targeted questions from any resume."),
        (PROMPT_INTAKE_XML, _GENERIC_INTAKE_XML,
         "Builds resume_data.xml from resume + Q&A answers."),
        (PROMPT_INTAKE_XML_DIRECT, _GENERIC_INTAKE_XML_DIRECT,
         "Builds resume_data.xml from resume PDF text only (no Q&A)."),
        (PROMPT_EXPERIENCE_ENHANCE, _GENERIC_EXPERIENCE_ENHANCE,
         "Rewrites experience bullets to be punchy and achievement-focused."),
        (PROMPT_JOB_EXPERIENCE_PRIORITY, _GENERIC_JOB_EXPERIENCE_PRIORITY,
         "Reorders and selects experience/project bullets by job relevance."),
    ]:
        if not prompt_exists(name):
            set_prompt(name, content, desc)
            created += 1
        else:
            skipped += 1

    return {"created": created, "skipped": skipped}


