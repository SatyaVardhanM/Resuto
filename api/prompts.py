# -- Database helpers ---------------------------------------------
def _connect() -> sqlite3.Connection:
    conn = sqlite3.connect(DB_FILE)
    conn.row_factory = sqlite3.Row
    return conn


def _ensure_table() -> None:
    """Create the prompts table if it does not exist."""
    conn = _connect()
    try:
        conn.execute("""
            CREATE TABLE IF NOT EXISTS prompts (
                name        TEXT PRIMARY KEY,
                content     TEXT NOT NULL DEFAULT '',
                description TEXT NOT NULL DEFAULT '',
                created_at  TEXT,
                updated_at  TEXT
            )
        """)
        conn.commit()
    finally:
        conn.close()


# -- Public API ---------------------------------------------------
def get_prompt(name: str) -> str:
    """
    Returns the prompt content for the given name.
    Raises a clear error if the prompt does not exist yet.
    """
    _ensure_table()
    conn = _connect()
    try:
        row = conn.execute(
            "SELECT content FROM prompts WHERE name = ?", (name,)
        ).fetchone()
    finally:
        conn.close()

    if row:
        return row["content"]

    if name in PERSONAL_PROMPTS:
        raise ValueError(
            f"[SETUP REQUIRED] Personal prompt '{name}' is missing from prompts.db. "
            f"Run:  python migrate_prompts.py  (existing users) "
            f"OR  python resume_intake.py  (new users)"
        )
    else:
        raise ValueError(
            f"[SETUP REQUIRED] Prompt '{name}' is missing from prompts.db. "
            f"Run:  python migrate_prompts.py"
        )



def delete_prompt(name: str) -> None:
    """Delete a prompt from the DB so it regenerates on next run."""
    try:
        db = _db_file()
        con = sqlite3.connect(db)
        con.execute("DELETE FROM prompts WHERE name = ?", (name,))
        con.commit()
        con.close()
    except Exception:
        pass

def get_profile_hash(profile: dict) -> str:
    """Stable hash of the profile used to generate personal prompts."""
    import hashlib, json as _j
    key_fields = {
        "name":             profile.get("name", ""),
        "years_experience": profile.get("years_experience", 0),
        "summary":          profile.get("summary", "")[:200],
        "skills":           profile.get("skills", {}),
        "experience":       [
            {"title": j.get("title",""), "company": j.get("company","")}
            for j in profile.get("experience", [])[:4]
        ],
    }
    raw = _j.dumps(key_fields, sort_keys=True)
    return hashlib.sha256(raw.encode()).hexdigest()[:16]


def store_profile_hash(h: str) -> None:
    """Store the profile hash used to generate personal prompts."""
    set_prompt("_profile_hash", h, "Hash of profile when personal prompts were last generated.")


def get_stored_profile_hash() -> str:
    """Return the stored profile hash, or empty string if never set."""
    try:
        return get_prompt("_profile_hash") or ""
    except (ValueError, Exception):
        return ""


def personal_prompts_match_profile(profile: dict) -> bool:
    """True if personal prompts were generated from the current profile."""
    try:
        if not profile:
            return True
        stored = get_stored_profile_hash()
        if not stored:
            return False
        return stored == get_profile_hash(profile)
    except Exception:
        return True   # any error — don't force regenerate


def set_prompt(name: str, content: str, description: str = "") -> None:
    """Inserts or updates a prompt. Safe to call multiple times."""
    _ensure_table()
    now  = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    conn = _connect()
    try:
        conn.execute("""
            INSERT INTO prompts (name, content, description, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?)
            ON CONFLICT(name) DO UPDATE SET
                content     = excluded.content,
                description = excluded.description,
                updated_at  = excluded.updated_at
        """, (name, content, description, now, now))
        conn.commit()
    finally:
        conn.close()


def prompt_exists(name: str) -> bool:
    """Returns True if this prompt is already in the database."""
    _ensure_table()
    conn = _connect()
    try:
        row = conn.execute(
            "SELECT 1 FROM prompts WHERE name = ?", (name,)
        ).fetchone()
        return row is not None
    finally:
        conn.close()


def list_prompts() -> list:
    """Returns all prompts with name, description, updated_at."""
    _ensure_table()
    conn = _connect()
    try:
        rows = conn.execute(
            "SELECT name, description, updated_at FROM prompts ORDER BY name"
        ).fetchall()
        return [dict(r) for r in rows]
    finally:
        conn.close()


def all_prompts_ready() -> bool:
    """Returns True if all required prompts exist in the database."""
    required = [
        PROMPT_RELEVANCE_CHECK,
        PROMPT_RESUME_TAILOR,
        PROMPT_INTAKE_QUESTIONS,
        PROMPT_INTAKE_XML,
        PROMPT_EXPERIENCE_ENHANCE,
        PROMPT_JOB_EXPERIENCE_PRIORITY,
    ]
    return all(prompt_exists(n) for n in required)


