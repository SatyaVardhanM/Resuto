"""
Manages AI prompts in a local SQLite database (prompts.db).

How it works:
  - Two prompts are GENERIC (same for every user):
      intake_questions   - generates interview questions from any resume
      intake_xml_builder - builds XML from any Q&A answers
    These are seeded from static text the first time they are needed.

  - Two prompts are PERSONAL (specific to each user):
      relevance_check  - describes THIS person's specialization and what to skip
      resume_tailor    - describes THIS person's background for honest tailoring
    These are GENERATED by Claude reading the user's real profile.
    They are never hardcoded. They are created once and stored.

Users never need to open this file, the database, or edit anything.
Everything is automatic.
"""

import os
import json
import sqlite3
from datetime import datetime

def _db_path(path=None): return _db_file(path)

def _db_file() -> str:
    """Resolve prompts.db path — exe-aware."""
    import sys as _sys
    if getattr(_sys, "frozen", False):
        base = os.path.dirname(_sys.executable)
    else:
        import sys as _sys3
        base = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        if not os.path.isdir(os.path.join(base, "api")):
            base = os.path.dirname(os.path.abspath(_sys3.executable))
    data_dir = os.path.join(base, "data")
    os.makedirs(data_dir, exist_ok=True)
    return os.path.join(data_dir, "prompts.db")

DB_FILE = _db_file()

